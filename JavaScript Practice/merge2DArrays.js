const mergeArrays = function (nums1, nums2) {
    const resultMap = new Map();

    for (const [key, value] of nums1) {
        resultMap.set(key, (resultMap.get(key) || 0) + value)
    }
    for (const [key, value] of nums2) {
        resultMap.set(key, (resultMap.get(key) || 0) + value)
    }
    return ([...resultMap]).sort((a, b) => a[0] - b[0]);
};

